// What is Middle Ware Function?
// A middleware is a function that takes in an HTTP request and response object, plus the next middleware function in the chain.

// Middle ware functions have access to:
// 1. request object
// 2. response object
// 3. next function in req,res cycle

// Middleware functions can perform the following tasks:
// 1.Execute any code.
// 2.Make changes to the request and the response objects.
// 3.End the request-response cycle.
// 4.Call the next middleware in the stack.

// Types of MiddleWare
// Application-level middleware
// Router-level middleware
// Error-handling middleware
// Built-in middleware
// Third-party middleware

